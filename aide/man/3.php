<!-- 3. Modifier une fiche -->
<p class="aide_chapitre"><a name="3"></a>
3. Modifier une fiche
</p>

<!-- 3.1 Quelles fiches puis-je modifier ? -->
<p class="aide_section"><a name="3.1"></a>
3.1 Quelles fiches puis-je modifier ?
</p>

<p class="aide_texte">
	Vous ne pouvez modifier que votre propre fiche, c'est-à-dire la fiche à votre nom, celle que vous avez créée et dont vous avez défini la question personnelle et la réponse secrète (<a href="#2.7">v. n° 2.7</a>).
</p>

<p class="aide_section"><a name="3.2"></a>
3.2 Pourquoi ne puis-je pas modifier toutes les fiches ?
</p>

<p class="aide_texte">
	Pour des raisons de sécurité des données, chaque utilisateur ne peut modifier que sa propre fiche. Pour plus d'informations, <a href="#2.7">v. n° 2.7</a>.
</p>

<p class="aide_section"><a name="3.3"></a>
3.3 Comment puis-je modifier une fiche ?
</p>

<p class="aide_texte">
	Pour modifier votre fiche, il vous faudra procéder ainsi:
	<ol>
		<li />Vous connecter à l'annuaire. (<a href="#1.2">v. n° 1.2</a>)
		<li />Afficher la liste des fiches. (<a href="#5.2">v. n° 5.2 </a>)
		<li />Choisir votre fiche dans la liste et l'afficher. (<a href="#5.5">v. n° 5.5</a>)
		<li />Dans le menu <b>Fiches</b>, choisir <b>Modifier la fiche</b>.
		<li />Effectuer les modifications désirées.
		<li />Indiquer la réponse secrète à votre question personnelle. (<a href="#2.7">v. n° 2.7</a>)
		<li />Enregistrer la fiche en cliquant sur le bouton "Enregistrer" en bas de la page.
	</ol>
</p>

<!-- 3.4 Que faire si j'ai oublié la réponse secrète à ma question personnelle ? -->
<p class="aide_section"><a name="3.4"></a>
3.4 Que faire si j'ai oublié la réponse secrète à ma question personnelle ?
</p>

<p class="aide_texte">
	La réponse secrète est chiffrée dans la base de données avec un algorithme à sens unique. Il est donc impossible de retrouver cette réponse si vous veniez à l'oublier. Les administrateurs de l'annuaire pourront supprimer votre fiche pour vous permettre d'en créer une nouvelle, mais ils ne pourront pas vous aider à récupérer votre réponse secrète.
</p>

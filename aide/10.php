10. Changer la langue
</p>

<!-- 10.1 Comment changer la langue de l'annuaire ? -->
<p class="aide_section"><a name="10.1"></a>
10.1 Comment changer la langue de l'annuaire ?
</p>

<p class="aide_texte">
	Pour changer la langue dans laquelle l'interface de l'annuaire est affichée, cliquez sur <b>Langue</b> dans le menu de navigation, puis sur la langue désirée dans le sous-menu.
</p>

<!-- 10.2 Qu'implique un tel changement de langue ? -->
<p class="aide_section"><a name="10.2"></a>
10.2 Qu'implique un tel changement de langue ?
</p>

<p class="aide_texte">
	Le changement de la langue a pour effet de traduire dans la langue désirée l'interface de l'annuaire. Il ne s'agit que de l'interface, c'est-à-dire le <i>contenant</i>, les boutons et les menus. Le <i>contenu</i> de chaque fiche n'est en aucun cas traduit: chaque utilisateur rédige sa fiche dans la langue qu'il désire utiliser, parmi les langues autorisées par les administrateurs de l'annuaire.
</p>

<p class="aide_texte">
	Par défaut, l'interface est affichée en français. Des traductions en anglais et en espagnol sont disponibles.
</p>

<!-- 10.3 Pourquoi mon choix de langue n'est-il pas conservé d'une session à l'autre ? -->
<p class="aide_section"><a name="10.3"></a>
10.3 Pourquoi mon choix de langue n'est-il pas conservé d'une session à l'autre ?
</p>

<p class="aide_texte">
	Le logiciel de l'annuaire contient un système de détection de la langue utilisée par le navigateur et/ou le système d'exploitation. Ainsi, par exemple, si vous utilisez une version de Windows en français, l'interface de l'annuaire s'affichera en français, même si vous consultez celui-ci depuis votre ordinateur portable dans un hotel de Madrid. En revanche, si vous allez dans un cyber-café madrilène, l'interface sera affichée en espagnol !
</p>

<p class="aide_texte">
	Mais comme la langue "devinée" n'est pas forcément la langue souhaitée par l'utilisateur (l'hypothèse du cyber-café madrilène), il est possible de changer manuellement la langue (<a href="#10.1">v. n°10.1</a>).
</p>

<p class="aide_texte">
	Pour des raisons d'opportunité (<a href="#1.1">v. n°1.1</a>), l'annuaire ne différencie pas les utilisateurs les uns des autres. Il ne peut donc pas savoir à l'avance la langue que vous préférez.
</p>

<p class="aide_texte">
	Pour toute la durée de la session (<a href="#1.3">v. n°1.3</a>), la langue que vous avez choisie est mémorisée. En revanche, dès que vous fermez cette session, l'annuaire "oublie" qui vous êtes. Si vous revenez, il ne se souviendra plus de votre choix précédent, et affichera l'interface dans la langue de votre navigateur et/ou système d'exploitation.
</p>